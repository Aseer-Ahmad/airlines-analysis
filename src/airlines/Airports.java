
package airlines;


public class Airports {
    
    private int airpID;
    private String airpname;
    private String city;
    private String country;
    private String IATA;
    private String ICAO;
    private double latitude;
    private double longitude;
    private int altitude;
    private float timezh;
    private String DST;

    public int getAirpID() {
        return airpID;
    }

    public void setAirpID(int airpID) {
        this.airpID = airpID;
    }

    public String getAirpname() {
        return airpname;
    }

    public void setAirpname(String airpname) {
        this.airpname = airpname;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getIATA() {
        return IATA;
    }

    public void setIATA(String IATA) {
        this.IATA = IATA;
    }

    public String getICAO() {
        return ICAO;
    }

    public void setICAO(String ICAO) {
        this.ICAO = ICAO;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongtitude() {
        return longitude;
    }

    public void setLongtitude(double longitude) {
        this.longitude = longitude;
    }

    public int getAltitude() {
        return altitude;
    }

    public void setAltitude(int altitude) {
        this.altitude = altitude;
    }

    public float getTimezh() {
        return timezh;
    }

    public void setTimezh(float timezh) {
        this.timezh = timezh;
    }

    public String getDST() {
        return DST;
    }

    public void setDST(String DST) {
        this.DST = DST;
    }
    
    
    
    
    
    
    
    
}
